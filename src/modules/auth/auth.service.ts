  async resetPassword({ email, token, password }) {
    try {
      const user = await UserRepository.exist({
        field: 'email',
        value: email,
      });

      if (user) {
        const existToken = await UcodeRepository.validateToken({
          email: email,
          token: token,
        });

        if (existToken) {
          await UserRepository.changePassword({
            email: email,
            password: password,
          });

          // delete otp code
          await UcodeRepository.deleteToken({
            email: email,
            token: token,
          });

          return {
            success: true,
            message: 'Password updated successfully',
          };
        } else {
          return {
            success: false,
            message: 'Invalid token',
          };
        }
      } else {
        return {
          success: false,
          message: 'Email not found',
        };
      }
    } catch (error) {
      return {
        success: false,
        message: error.message,
      };
    }
  }

  async verifyEmail({ email, token }) {
    try {
      const user = await UserRepository.exist({
        field: 'email',
        value: email,
      });

      if (user) {
        const existToken = await UcodeRepository.validateToken({
          email: email,
          token: token,
        });

        if (existToken) {
          // Only approve patients automatically when they verify email
          // Doctors and shop_owners need admin approval
          const updateData: any = {
            email_verified_at: new Date(Date.now()),
          };

          // Only set approved_at for patients
          // Doctors and shop_owners must be approved by admin
          if (user.type === 'patient') {
            updateData.approved_at = new Date(Date.now());
          }

          await this.prisma.user.update({
            where: {
              id: user.id,
            },
            data: updateData,
          });

          // delete otp code
          await UcodeRepository.deleteToken({
            email: email,
            token: token,
          });

          // Return appropriate message based on user type
          if (user.type === 'patient') {
            return {
              success: true,
              message: 'Email verified successfully',
            };
          } else {
            return {
              success: true,
              message:
                'Email verified successfully. Your account is pending admin approval. You will be able to log in once approved.',
            };
          }
        } else {
          return {
            success: false,
            message: 'Invalid token',
          };
        }
      } else {
        return {
          success: false,
          message: 'Email not found',
        };
      }
    } catch (error) {
      return {
        success: false,
        message: error.message,
      };
    }
  }

